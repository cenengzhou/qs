
  CREATE OR REPLACE FORCE VIEW PCMSDATAPROD.V_QS_SC_PACKAGE (JDE_JOBNO, JOBNO, JOB_DESCRIPTION, COMPANY, DIVISION, DEPARTMENT, SOLOJV, JVPERCENTAGE, JDE_PACKAGENO, PACKAGENO, SC_DESCRIPTION, PACKAGETYPE, JDE_VENDORNO, VENDORNO, PACKAGESTATUS, SCSTATUS, SCNATURE, ORIGINALSCSUM, APPROVEDVOAMOUNT, REMEASUREDSCSUM, APPROVALROUTE, RETENTIONTERMS, MAXRETPERCENT, INTERIMRETPERENT, MOSRETPERENT, RETAMOUNT, ACCUMLATEDRET, RETRELEASE, PAYMENTINFO, PAYMENTCURRENCY, EXCHANGERATE, PAYMENTTERMS, SCTERM, CPFCALCULATION, CPFBASEPERIOD, CPFBASEYEAR, FORMOFSUBCONTRACT, INTERNALJOBNO, PAYMENTSTATUS, SUBMITTEDADDENDUM, LABOURINCLUDED, PLANTINCLUDED, MATERIALINCLUDED, SPLITTERMINATESTATUS, SCCREATEDDATE, LATESTADDENDUMVALUEUPDATEDDATE, FIRSTPAYMENTCERTISSUEDDATE, LASTPAYMENTCERTISSUEDDATE, FINALPAYMENTISSUEDDATE, SCAWARDAPPROVALREQUESTSENTDATE, SCAPPROVALDATE, TOTALPOSTEDCERTAMT, TOTALCUMCERTAMT, TOTALPOSTEDWDAMT, TOTALCUMWDAMT, TOTALCCPOSTEDCERTAMT, TOTALMOSPOSTEDCERTAMT) AS 
  SELECT LPAD (JOBNO, 12, ' ') AS JDE_JOBNO,
          JOBNO,
          JOB_INFO.DESCRIPTION AS JOB_DESCRIPTION,
          COMPANY,
          DIVISION,
          DEPARTMENT,
          SOLOJV,
          JVPERCENTAGE,
          TO_NUMBER (PACKAGENO) AS JDE_PACKAGENO,
          PACKAGENO,
          SUBCONTRACT.DESCRIPTION AS SC_DESCRIPTION,
          PACKAGETYPE,
          TO_NUMBER (VENDORNO) AS JDE_VENDORNO,
          VENDORNO,
          PACKAGESTATUS,
          SCSTATUS,
          SCNATURE,
          ORIGINALSCSUM,
          APPROVEDVOAMOUNT,
          REMEASUREDSCSUM,
          APPROVALROUTE,
          RETENTIONTERMS,
          SUBCONTRACT.MAXRETPERCENT,
          INTERIMRETPERENT,
          SUBCONTRACT.MOSRETPERENT,
          RETAMOUNT,
          ACCUMLATEDRET,
          RETRELEASE,
          PAYMENTINFO,
          PAYMENTCURRENCY,
          EXCHANGERATE,
          PAYMENTTERMS,
          SCTERM,
          SUBCONTRACT.CPFCALCULATION,
          SUBCONTRACT.CPFBASEPERIOD,
          SUBCONTRACT.CPFBASEYEAR,
          FORMOFSUBCONTRACT,
          INTERNALJOBNO,
          PAYMENTSTATUS,
          SUBMITTEDADDENDUM,
          LABOURINCLUDED,
          PLANTINCLUDED,
          MATERIALINCLUDED,
          SPLITTERMINATESTATUS,
          SCCREATEDDATE,
          LATESTADDENDUMVALUEUPDATEDDATE,
          FIRSTPAYMENTCERTISSUEDDATE,
          LASTPAYMENTCERTISSUEDDATE,
          FINALPAYMENTISSUEDDATE,
          SCAWARDAPPROVALREQUESTSENTDATE,
          SCAPPROVALDATE,
          TOTALPOSTEDCERTAMT,
          TOTALCUMCERTAMT,
          TOTALPOSTEDWDAMT,
          TOTALCUMWDAMT,
          TOTALCCPOSTEDCERTAMT,
          TOTALMOSPOSTEDCERTAMT
     FROM PCMSDATAPROD.JOB_INFO, PCMSDATAPROD.SUBCONTRACT
    WHERE JOB_INFO_ID = JOB_INFO.ID;

